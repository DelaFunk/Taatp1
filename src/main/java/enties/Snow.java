package enties;

public class Snow {

	private long idSnow;
	private String label;
	
	public Snow(long idSnow, String label) {
		super();
		this.idSnow = idSnow;
		this.label = label;
	}

	public long getIdSnow() {
		return idSnow;
	}

	public void setIdSnow(long idSnow) {
		this.idSnow = idSnow;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
	
		
}
